export function searchZones(query: string): RufaaZone[] {
  if (!query.trim()) return rufaaZones;
  const q = query.trim();
  return rufaaZones.filter((z) => z.nameAr.includes(q));
}

export function getZonesByCategory(category: ZoneCategory): RufaaZone[] {
  return rufaaZones.filter((z) => z.category === category);
}
